<h6> 2. WHAT TYPES OF COOKIES? WHAT ARE THEIR PURPOSES? </h6>
<p> First, we have to differentiate between permanent cookies and session cookies. </p>
<p> PERMANENT COOKIES: These are those that are stored in your browser, even after you have finished accessing the site. </p>
<p> TEMPORARY COOKIES: These are only collected while you access the site, after logging out they are not stored. </p>
<p> There are also different types of cookies, each of which has a specific purpose. </p>
<p> COOKIES REQUIRED: As the name implies, these cookies are necessary to allow certain features of the website and the use of our services. </p>
<p> ANALYTICAL COOKIES: Analytical cookies are those that enable the collection and analysis of statistical data, allowing you to check which pages you have accessed, how many times these pages have been accessed, which aim to improve the performance and functioning of the website. </p>
<p> FUNCTIONALITY COOKIES: These cookies are responsible for keeping the preferences of your use on our website, so that you do not have to select them again. </p>
<p> ADVERTISING COOKIES: These types of cookies are those designed to collect information to enable advertisements and personalize publication. </p>
